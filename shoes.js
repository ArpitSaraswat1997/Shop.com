function changeData() {
  var arr = data
    .filter(function (e) {
      if (filter_data.gender == "none") return true;
      return e.gender.toLowerCase() == filter_data.gender.toLowerCase();
    })
    .filter(function (e) {
      if (filter_data.color == "none") return true;
      return e.color.toLowerCase() == filter_data.color.toLowerCase();
    })
    .filter(function (e) {
      if (filter_data.size == "none") return true;
      return e.size == filter_data.size;
    })
    .filter(function (e) {
      if (filter_data.range.min == "none") return true;
      var price_ = convert(e.price);
      console.log(price_);
      if (filter_data.range.max == " Below")
        return price_ < +convert(filter_data.range.min.toString());
      if (filter_data.range.max == " Above")
        return price_ >= +convert(filter_data.range.min.toString());
      return (
        price_ >= +convert(filter_data.range.min.toString()) &&
        price_ <= +convert(filter_data.range.max.toString())
      );
    })
    .filter(function (e) {
      if (filter_data.sale) {
        return e.stikep;
      }
      return true;
    });

  if (filter_data.sort != "none") {
    if (filter_data.sort == "h-to-l") {
      arr.sort(function (a, b) {
        return convert(b.price) - convert(a.price);
      });
    }
    if (filter_data.sort == "l-to-h") {
      arr.sort(function (a, b) {
        return convert(a.price) - convert(b.price);
      });
    }
    if (filter_data.sort == "cashback") {
      arr.sort(function (a, b) {
        return convert(b.price) - convert(a.price);
      });
    }
  }
  display(arr);
}

function display(array) {
  grid.innerHTML = null;
  array.map(function (e, i) {
    var div = document.createElement("div");
    var img = document.createElement("img");
    img.setAttribute("src", e.image);
    var h3 = document.createElement("h3");
    h3.innerText = e.name;
    var p1 = document.createElement("p");
    p1.innerText = "Sold by " + "Cutter & Buck";
    var p2 = document.createElement("p");
    if (e.stikep) {
      var span = document.createElement("u");
      span.innerText = e.stikep + " ";
      p2.appendChild(span);
    }
    p2.innerText += e.price;
    var p3 = document.createElement("p");
    p3.innerText =
      "+ $" +
      ((i % 7) + 1) +
      " / " +
      (Math.floor(Math.random() * 6) + 1) +
      "% Cashback";
    div.append(img, h3, p1, p2, p3);
    if (e.free_shipping) {
      var free = document.createElement("p");
      free.innerText = "Free Shipping";
      div.append(free);
    }
    var div1 = document.createElement("div");
    div1.setAttribute("id", "btn");
    var see_details = document.createElement("button");
    see_details.setAttribute("id", "see_details");
    see_details.innerText = "See details";
    see_details.addEventListener("click", function () {
      var x = Object.assign({}, e);
      localStorage.setItem("product", JSON.stringify(x));
      window.location.href = "product-details.html";
    });
    var addToCart = document.createElement("button");
    addToCart.addEventListener("click", function () {
      addCart(e.id);
    });
    addToCart.innerText = "Add To Cart";
    addToCart.setAttribute("id", "addCart");
    div1.append(see_details, addToCart);
    div.append(div1);
    grid.append(div);
  });
  if (array.length == 0) {
    grid.innerHTML = `<p>No products to display</p>`;
  }
}

